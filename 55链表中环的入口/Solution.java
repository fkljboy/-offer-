public class Solution {

    public ListNode EntryNodeOfLoop(ListNode pHead)
    {
         if (pHead == null || pHead.next == null)
        return null;
    ListNode slow = pHead, fast = pHead;
    do {
        fast = fast.next.next;
        slow = slow.next;
    } while (slow != fast);
    fast = pHead;
    while (slow != fast) {
        slow = slow.next;
        fast = fast.next;
    }
    return slow;
    }
}
/* 根据计算可以得到，快慢指针的相遇点到环入口的距离就等于起始点到环入口点的距离
所以计算得以简化 */